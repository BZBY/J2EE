import turtle
turtle.setup(750, 450, 350, 150)
turtle.penup()
turtle.fd(-250)
turtle.pendown()
turtle.pensize(15)
turtle.pencolor("blue")
turtle.seth(-40)
for i in range(2):
    turtle.circle(50, 80)
    turtle.circle(-50, 80)
turtle.circle(40, 40)
turtle.fd(40)
turtle.circle(16, 90)
turtle.fd(20)
turtle.circle(16, 90)
turtle.fd(10)
turtle.circle(16, 120)
turtle.fd(-20)
turtle.done()
